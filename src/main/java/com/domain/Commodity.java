package com.domain;

public class Commodity {
	
	private Integer id;
	private String commName;
	private String commPrice;
	private String author;
	private String press;
	private String pubData;
	private String isbn;
	private String commLabel;
	private String commCatalog;
	private String preface;
	private String pictureName;
	private String parentCatalogNum;//一级目录
	private String childCatalogNum;//二级目录
	private String score;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getCommName() {
		return commName;
	}
	public void setCommName(String commName) {
		this.commName = commName;
	}
	public String getCommPrice() {
		return commPrice;
	}
	public void setCommPrice(String commPrice) {
		this.commPrice = commPrice;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	public String getPress() {
		return press;
	}
	public void setPress(String press) {
		this.press = press;
	}
	public String getPubData() {
		return pubData;
	}
	public void setPubData(String pubData) {
		this.pubData = pubData;
	}
	public String getIsbn() {
		return isbn;
	}
	public void setIsbn(String isbn) {
		this.isbn = isbn;
	}
	public String getCommLabel() {
		return commLabel;
	}
	public void setCommLabel(String commLabel) {
		this.commLabel = commLabel;
	}
	public String getCommCatalog() {
		return commCatalog;
	}
	public void setCommCatalog(String commCatalog) {
		this.commCatalog = commCatalog;
	}
	public String getPreface() {
		return preface;
	}
	public void setPreface(String preface) {
		this.preface = preface;
	}
	public String getPictureName() {
		return pictureName;
	}
	public void setPictureName(String pictureName) {
		this.pictureName = pictureName;
	}
	public String getParentCatalogNum() {
		return parentCatalogNum;
	}
	public void setParentCatalogNum(String parentCatalogNum) {
		this.parentCatalogNum = parentCatalogNum;
	}
	public String getChildCatalogNum() {
		return childCatalogNum;
	}
	public void setChildCatalogNum(String childCatalogNum) {
		this.childCatalogNum = childCatalogNum;
	}
	public String getScore() {
		return score;
	}
	public void setScore(String score) {
		this.score = score;
	}
	
	


}
